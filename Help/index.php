<p class="history">Բոլոր հաշվետվությունները իրենից ներկայացնում են աղյուսակներ։ Տվյալները բեռնելու համար հաճախ անհրաժեշտ է նշել ժամանակաշրջանը, ինչպես նաև այլ պարամետրերը, որոնք հադիսանում են ֆիլտր տվյալների համար։ Բոլոր աղյուակներում սյունի անվան վրա կարելի է կտացնել մկնիկի աջ կոճակով և կիռարել արագ ֆիլտր, ինչպես Excel-ում։ Այլ ուրիշ արագ փնտրելու ձև աղյուսակում հանդիսանում է միաժամանակ Ctrl + F կոճակների կոմբինացիան, որը աղյուսակի վերևի մասում բացում է հատուկ դաշտ, որտեղ կարելի գրել բառ, կամ բառի մի մասը և ըստ դրա աղյուսակում մնալու են բոլոր այն տողերը, որոնք պարունակում են գրվածը։ Փնտրելու պարամետրերի պատուհանը բապկացած է երկու էջից։ Առաջին էջում նշվում են այն պայմանները, ըստ որի բեռնվելու են տվյալները, իսկ երկրորդ էջում կարելի է նշել այն հաշվետվության սյուները, որոնք պետք է ներարած լինեն հաշվետվության մեջ։ Ցուցադրելով, կամ թաքցնելով հաշվետվության սյուները ֆիլտրի միջոցով կարելի է ստանալ տարբեր ձևերի հաշվետվություններ։Եթե հաշվետվությունը պարունակում է "Համար" սյունը, ապա երկու անգամ կտացնելով մկնիկի ձախ կոճակով տողի վրա բացվում է համապատասպան փասթաթուղթը, որը կարելի է, օրինակ, խմբագրել։</p>
<p class="header">Փասթաթղթերի մատյան</p>
<p class="history">Այստեղ գտնվում են բոլոր գրանցումները, այսնքն պահեստի մուտքի, ելքի, տեղաշարժի, դրամարկղի փասթաթղթերը և այլն։ Վերևի գործիքները հնարավորություն են տալիս հաշվետվության մեջ նշված տողերի փասթաթղթերը գրանցել, դարձնել սևագր, հեռացնել։ Պետք է նշել, որ որոշ փասթաթղթերը կապված են այլ ուրիշ փասթաղթերի հետ և անմիջապես խմբագրման ենթակա չէն։</p>
<p class="header">Պահեստի փասթաթղթեր</p>
<p class="history">Այստեղ գտնում են միայն պահեստի մուտքերի, ելքերի, տեղաշարժի, կոմպլեկտավորման փասթաթղթերը։</p>
<p class="header">Ապրանքներ պահեստում</p>
<p class="history">Ապրանքներ պահեստում աշխատում է երկու տարբերակով, որը կարելի ընտրել պարամետրերի մեջ դենելով նշան "Ցուցադրել սևագրերը" դաշտում։ Եթե այդ նշանը դրված չէ, ապա պահեստում ցուցադրված ապրանքների քանակները կազմված են միայն գրանցված փասթաթղթերից։ Եթե նշանը դրված է, ապա ապրանքների քանակը կազմված է նաև սևագրեր փասթաթղթերից։</p>
<p class="header">Տեղաշարժ պահեստում</p>
<p class="history">Այս հաշվետվությունը արտացոլում է պահեստ մուտք և ելք եղաց ապրանքները, տող առ տող։ Հաճախ օգտագործվում է պահեստում ապրանքի քանակի առաջացման հասկանալու համար։</p>
<p class="header">Տեղաշարժ պահեստում, ապակոմպլեկտավորված</p>
<p class="history">Այն դեպքերում, երբ ապրանքի մի մասը պահեստում գտնվում է արկղների մեջ, իսկ մյուս մասը արկղերիծ հանած է և պահեստում երևում է հատով, տվյալ ապրանքի տեղաշարժի ընհանուր քանակը հասկանալու համար օգտագործվում է այս հաշվտվությունը։ Օրինակ, ապրանքի վաճառքը հնարավոր է և բլոկով և հատով։ Պահեստում առկա է 10 բլոկ խաղալիք, , և 5 հատ խաղալիք, որը հանվել բլոկից։ Մեկ բլոկի մեջ առկա է 10 հատ խաղալիք։ Ապա այս հաշվետվությունը ցույց կտա 10 x 10 + 5 = 105 հատ խաղալիք։ Այս հաշվետվությունը կաշխատի այն ժամանակ, եթե արկղով ապրանքի քարտի մեջ նշված է կոմպլեկտավորման բաժինում, որ ինքը բաղկացած է 10 հատ այլ ապրանքի միավորից։</p>
<p class="header">Ապակոմպլեկտավորված ապրանքներ պահեստում</p>
<p class="history">Այն դեպքերում, երբ ապրանքի մի մասը պահեստում գտնվում է արկղների մեջ, իսկ մյուս մասը արկղերիծ հանած է և պահեստում երևում է հատով, տվյալ ապրանքի ընհանուր քանակը հասկանալու համար օգտագործվում է այս հաշվտվությունը։ Օրինակ, ապրանքի վաճառքը հնարավոր է և բլոկով և հատով։ Պահեստում առկա է 10 բլոկ խաղալիք, , և 5 հատ խաղալիք, որը հանվել բլոկից։ Մեկ բլոկի մեջ առկա է 10 հատ խաղալիք։ Ապա այս հաշվետվությունը ցույց կտա 10 x 10 + 5 = 105 հատ խաղալիք։ Այս հաշվետվությունը կաշխատի այն ժամանակ, եթե արկղով ապրանքի քարտի մեջ նշված է կոմպլեկտավորման բաժինում, որ ինքը բաղկացած է 10 հատ այլ ապրանքի միավորից։</p>
<p class="header">Ապրանքի ելք ըստ վաճառքի</p>
<p class="history">Այս հաշվետվությունը արտացոլում է պահեստում ապրանքի շարժը նշված ժամանակաշրջանի համար։ Ցուցադևվում է ապրանքի նախնական մնացորդը, մուտքը, ելքը, հաշվարկային մնացորդը, գույքագրման տվյալները և տարբերություն /պակասնորդ կամ ավելցուկ/։ Ընդ որում այստեղ նաև հնարավոր է կատարել պահեստում ապրանքի քանակի ուղղում։ Գուքագրման դաշտում ապրանքի տողի վրա եկու անգամ կտացնելուց հետո բացվում է դաշտ, որտեղ անմիջապես հնարավոր է մուտք անել ապրանքի փաստացի քանակը և միանգամից տեսնել տարբերությունը ծրագրի քանակի հետ։ Բոլոր ապրանքների փաստացի քանակները մուտքագրելուց հետո գործիքների "Արտահանել մուտք/ելք" կոճակը սեխմելով ստանում ենք պահեստի մուտք և պահեստի ելք փասթաթղթերը, որպեսզի մուտքագրվի ապրանքի ավելցուկը և ելքագրվի ապրանքի պակասնորդը ծրագրի և փաստացի քանակները համապատասխանացնելու նխատակով։</p>
<p class="header">Վաճառք, ընդհանուր</p>
<p class="history">Վաճարքների ըստ կտրոնների հաշվտվություն, արտացոլում է վաճառքը ըստ գործարքների։</p>
<p class="header">Վաճառք, ուտեստներ</p>
<p class="history">Ռեստորանի, սրճարանի կամ արագ սննդի կետում վաճառքի ամփոփումը ըստ ուտեստների։</p>
<p class="header">Վաճառք պահեստից</p>
<p class="history">Ռեստորանի, սրճարանի կամ արագ սննդի կետում վաճառքի ամփոփումը ըստ ուտեստի բաղադրատոմսով ելքագրված ապրանքները պահեստից։ Այս դեպքում նաև հանդիսանում է ուտեստների ինքառժեքների հաշվետվություն։ Եթե համակարգը կիռարվում է խանութի վաճառատեղի համար, ապա այս հաշվետվությունը արտացոլում է վաճառքը ըստ տեսականի։</p>
<p class="header">Դրամարկղ</p>
<p class="history">Դրամային գործարքները և հաշվետվությունները գտնվում են այս բաժնում։ Երբ վաճառքի ծրագրով կատարվում է գործարք, կարգավորումներում նշված դրամարկղում ավտոմատ գրանցվում է դրամի մուտք։ Այլ ավտոմատ գրանցում տեղի ունենում պահեստի մուտքի փասթաթուղթ ստեղծելու ժամանակ, մնացաց բոլոր դեպքերում դրամարկղում գործարքները կատարվում են մարդու կողմից։</p>
