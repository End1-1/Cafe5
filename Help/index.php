<p class="header">Ճաշացանկ</p>
<p class="history">Ճաշացանկը ունի հետևյալ կառուցվացք։ Յուրաքանչուրը ուտեստը պատկանում է տեսակին։ Ուտեստի տեսակը իր հերթին պատկանում է ինչ որ բաժնին, որը իր հերթին պատկանում է ճաշացանկին։ Օրինակ Ճաշացանկում գոյություն ունի Սպաս։ Իրա տեսեկը կարող է լինել Ապուր։ Ապուրը իր հերթին պականում է Խոհանոց բաժնին, որը հինմանակ ճաշացանկի անդամ է։ Այսպիսով, ճաշացանկը կազմելու համար անհրաժեշտ է կատարել հետեյալ քայլերը։</p>
<p>1. Գրանցել առնվազն մեկ ճաշացանկի անվանում "Ճաշացանկ / Ճաշացանկների ցանկ" բաժնում։</p>
<p>2. Գրանցել առնվազն մեկ ուտեստների տեսակների բաժնի անվանումը "Ճաշացանկ / Ուտեստների բաժիններ" բաժնում։</p>
<p>3. Գրանցել ուտեստների տեսակների անվանումները "Ճաշացանկ / Ուտեստների տեսակներ" բաժնում։</p>
