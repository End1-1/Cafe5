<p>1. առաջին տողում ժամանակաշրջանի սկզբում դրամի փասթացի մնացորդ</p>
<p>2. այնուհետև բոլոր դրամի մուտքերը և ելքերը, որոնք տեղի են ունեցել նշված ժամանակաշրջանում։</p>
<p>3. ժամանակաշրջանի վերջում դրամի փասթացի մնացորդ</p>
<p class="header">Դրամի շարժ</p>
<p class="history"><b>Դրամի շարժը</b> <b>Գործարքներ դրամարկղում</b>  հաշվետվության նման է, տարբերությունը դրանում է, որ սկզբնական և վերջնական մնացորդները չէն երևում։ Դրանց փախարեն արտացոլված է մուտքերի և ելքերի ընդհանուր գումարը։</p>
<p class="header">Դրամարղների ցանկ</p>
<p class="history">Այստեղ գրանցվում են դրամարղները, որոնց հետ հետագայում պետք է աշխատել։ Արնվազն մեկ դրամարկղ պետք է գրանցել կանխիկը մուտք անելու համար։ Եթե վճարումը կատարվելու է նաև քարտերի միջոցով, ապա առանձին գրանցվում է նաև անկանխիկ դրամարկղ։</p>
<p class="header">Աշխատավարձ</p>
<p class="history">Համակարգը հնարավորություն է տալիս գրանցել աշխտողների աշխատավարձը, ինչպես նաև կարող է հաշվարկել աշխատավարձը կախված վաճառքի գումարից, աշխատած ժամերից և այլն։ Այդ պայմանները առանձին քննարկվում են ծրագիրը ձերք բերման ժամանակ, քանի որ գոյություն ունեն բազմաթիվ ֆորմուլաներ։</p>
<p class="header">Նոր աշխատավարձի փասթաթուղթ</p>
<p class="history"><b>Նոր աշխատավարձի փասթաթուղթը</b> լրացնելու համար անհրաժեշտ է լրացնել հետևյալ դաշտերը</p>
